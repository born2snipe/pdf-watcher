package com.github.born2snipe.cli;

import net.sourceforge.argparse4j.inf.Argument;
import net.sourceforge.argparse4j.inf.ArgumentParser;
import net.sourceforge.argparse4j.inf.ArgumentParserException;
import net.sourceforge.argparse4j.inf.ArgumentType;

import java.io.File;
import java.io.IOException;

public class CanonicalFileArg implements ArgumentType<File> {
    @Override
    public File convert(ArgumentParser parser, Argument arg, String value) throws ArgumentParserException {
        try {
            return new File(value).getCanonicalFile();
        } catch (IOException e) {
            throw new ArgumentParserException(e, parser);
        }
    }
}
